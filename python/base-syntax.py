print("Hello, World!")

# Variables and data types
name = "Alice"
age = 25
is_student = True

# If-else condition
if age > 18:
    print("You are an adult.")
else:
    print("You are a minor.")

# For loop
for i in range(5):
    print(i)

# While loop
count = 0
while count < 5:
    print(count)
    count += 1

# Function definition
def greet(name):
    return f"Hello, {name}!"

print(greet("Bob"))

# List operations
fruits = ["apple", "banana", "cherry"]
fruits.append("orange")
print(fruits)

